void main()
{
  var X=5;
  switch(X)
  {
    case 0: 
    {
      print("Zero");
      break;
    }
     case 1: 
    {
      print("One");
      break;
    }
     case 2: 
    {
      print("Two");
      break;
    }
     case 3: 
    {
      print("Three");
      break;
    }
    default:
    {
    print("invalid");
    break;
    }
  }
}